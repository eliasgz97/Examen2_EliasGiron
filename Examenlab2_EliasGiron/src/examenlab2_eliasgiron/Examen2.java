    private void jb_agregarcompMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_agregarcompMouseClicked
        RAM r = new RAM(Integer.parseInt(tf_tamano.getText()), tf_marca.getText(), Integer.parseInt(tf_ensamblaje1.getText()));
        DiscoDuro hdd = new DiscoDuro(Integer.parseInt(tf_tamano1.getText()), tf_marca1.getText(), Integer.parseInt(tf_ensamblaje2.getText()));
        Bateria b = new Bateria(Integer.parseInt(tf_horas.getText()), tf_material.getText(), Integer.parseInt(tf_ensamblaje3.getText()));
        Teclado t = new Teclado(tf_materialtec.getText(), tf_colortec.getText(), Integer.parseInt(tf_ensamblaje4.getText()));
        Pantalla p = new Pantalla(tf_tactil.getText(), tf_tipo.getText(), Integer.parseInt(tf_ensamblaje5.getText()));
        Procesador pr = new Procesador(Integer.parseInt(tf_nucleos.getText()), tf_velocidad.getText(), Integer.parseInt(tf_ensamblaje6.getText()));
        Computadora c = new Computadora(tf_serie.getText(), tf_ano.getText(), tf_colorcomp.getText(), tf_materialcomp.getText(), r, hdd, b, t, p, pr);
        aensamblar.add(c);
        DefaultListModel modelo = (DefaultListModel) jl_computadoras.getModel();
        modelo.addElement(c);
        jl_computadoras.setModel(modelo);
        Tecnico tec = new Tecnico(tf_nombretec.getText(), Integer.parseInt(tf_edadtec.getText()), tf_genero.getText());
        tecnicos.add(tec);
        DefaultListModel modelo2 = (DefaultListModel) jl_tecnicos.getModel();
        modelo2.addElement(tec);
        OrdenEnsamblaje oe = new OrdenEnsamblaje(c, tec);
        ensamblajes.add(oe);
        DefaultListModel modelo3 = (DefaultListModel) jl_aensamblar.getModel();
        modelo3.addElement(oe);
        jl_aensamblar.setModel(modelo);
        JOptionPane.showMessageDialog(jd_agregarcom, "Agregado");
        admCompus ac = new admCompus("./compus.txt");
        ac.cargarArchivo();
        ac.getListacomputadoras().add(c);
        try {
            ac.escribirArchivo();
        } catch (IOException ex) {
            Logger.getLogger(Examen2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jb_agregarcompMouseClicked

