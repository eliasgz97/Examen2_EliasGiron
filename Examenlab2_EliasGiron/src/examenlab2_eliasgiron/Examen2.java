    private void jb_ejecutarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_ejecutarMouseClicked
        if (jl_aensamblar.getSelectedIndex() >= 0) {
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getRam().getTiempo();
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getHdd().getTiempo();
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getBateria().getTiempo();
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getTeclado().getTiempo();
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getPantalla().getTiempo();
            tiempo += ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora().getProcesador().getTiempo();
            ap = new admParte(pgb_ensamblaje);
            ap.setDuracion(tiempo);
            ap.setComputadora(ensamblajes.get(jl_aensamblar.getSelectedIndex()).getComputadora());
            ap.setTabla(jt_parte);
            pgb_ensamblaje.setMaximum(tiempo);
            ap.start();
            ap.tabla();
            if (ap.getBarra().getValue() == tiempo) {
                ap.getBarra().setValue(0); 
            }
        }
    }//GEN-LAST:event_jb_ejecutarMouseClicked

